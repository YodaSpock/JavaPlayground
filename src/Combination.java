public class Combination {
    Character color_1;
    Character color_2;
    Character color_3;
    Character color_4;

    public Combination(Character c1, Character c2, Character c3, Character c4) {
        color_1 = Character.toUpperCase(c1);
        color_2 = Character.toUpperCase(c2);
        color_3 = Character.toUpperCase(c3);
        color_4 = Character.toUpperCase(c4);
    }
}
